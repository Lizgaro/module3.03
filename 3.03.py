# Часть 1: Функция с параметрами по умолчанию
def print_params(a=1, b='строка', c=True):
    print(a, b, c)

# Примеры вызова функции с разным количеством аргументов
print_params()  # Все параметры по умолчанию
print_params(10)  # Заменён только первый параметр
print_params(b=25)  # Заменён только второй параметр
print_params(c=[1, 2, 3])  # Заменён только третий параметр

# Часть 2: Распаковка параметров
# Создаём список и словарь для распаковки
values_list = [42, 'Hello', False]
values_dict = {'a': 99, 'b': 'World', 'c': True}

# Распаковка списка и словаря в функцию
print_params(*values_list)  # Распаковка списка
print_params(**values_dict)  # Распаковка словаря

# Часть 3: Распаковка + отдельные параметры
values_list_2 = [54.32, 'Строка']

# Распаковка списка с добавлением третьего параметра
print_params(*values_list_2, 42)
